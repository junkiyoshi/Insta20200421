#include "ofApp.h"

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(60);
	ofSetWindowTitle("openFrameworks");

	ofBackground(239);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);
}

//--------------------------------------------------------------
void ofApp::update() {

	ofSeedRandom(39);

	this->face.clear();
	this->frame.clear();
	
	int len = 250;

	for (int z = -125; z <= 125; z += 10) {

		int param_start = ofMap(ofNoise(z * 0.005, ofGetFrameNum() * 0.005), 0, 1, 0, 100);
		for (int i = param_start; i < param_start + 50; i++) {

			this->setBoxToMesh(this->face, this->frame, glm::vec3(this->make_point(len, ofGetFrameNum() + i), z), len / 25);
		}
	}
}

//--------------------------------------------------------------
void ofApp::draw() {

	this->cam.begin();
	ofRotateX(270);

	ofSetColor(239, 39, 39);
	ofDrawSphere(80);

	ofSetColor(39);
	this->face.draw();

	ofSetColor(239);
	this->frame.drawWireframe();

	this->cam.end();
}

//--------------------------------------------------------------
void ofApp::setBoxToMesh(ofMesh& face_target, ofMesh& frame_target, glm::vec3 location, float size) {

	this->setBoxToMesh(face_target, frame_target, location, size, size, size);
}

